// Bitwise Operator

// +ve Number Concept

// '<<' leftShift Operator


console.log("00000000000000000000000000000010".length);

console.log(4 << 1);
console.log(8 << 2);

// 00000000000000000000000000000100;(4)
// 00000000000000000000000000001000;


// '>>' rightShift Operator


console.log("00000000000000000000000000000010".length);

console.log(4 >> 1);

console.log(8 >> 1);

00000000000000000000000000000100;
00000000000000000000000000000010;

00000000000000000000000000001000;
00000000000000000000000000000100;

// '>>>' zeroFillRightShift Operator


console.log("00000000000000000000000000000010".length);

console.log(4 >>> 1);

console.log(8 >>> 1);

00000000000000000000000000000100;
00000000000000000000000000000010;

00000000000000000000000000001000;
00000000000000000000000000000100;




// -ve Number Concept

// '<<' leftShift Operator


console.log("00000000000000000000000000000010".length);

// -4
// 00000000000000000000000000000100;(4)

// 11111111111111111111111111111011;
//  0000000000000000000000000000001 +
//  1111111111111111111111111111100  (-4)


console.log(-4 << 1);
console.log(-8 << 2);



// '>>' rightShift Operator


console.log("00000000000000000000000000000010".length);

console.log(-4 >> 1);

console.log(-8 >> 1);


// '>>>' zeroFillRightShift Operator

// -4
// 00000000000000000000000000000100;(4)

// 11111111111111111111111111111011;
//  0000000000000000000000000000001 +
// 01111111111111111111111111111100  


console.log(-4 >>> 1);

console.log(-8 >>> 1);





